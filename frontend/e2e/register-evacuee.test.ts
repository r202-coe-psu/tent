		await page
			.locator('[data-slot="form-item"]')
			.filter({ has: page.locator('label', { hasText: 'เพศ' }) })
			.locator('[data-slot="select-trigger"]')
			.click();
		await page.getByRole('option', { name: /ชาย \(Male\)/ }).click();
		await page.getByText('ไม่มีเบอร์โทร').click();

